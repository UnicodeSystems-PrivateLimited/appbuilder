    public static function getCurrencyOptions(): array {
        $options = [];
        foreach (self::CURRENCY_LIST as $code => $currency) {
            $options[] = [
                'label' => $currency . '(' . $code . ')',
                'value' => $code
            ];
        }
        return $options;
    }

    public static function getCurrencySymbol(string $currency_code): string {
        if (isset(self::CURRENCY_SYMBOL_LIST[$currency_code]) && !empty(self::CURRENCY_SYMBOL_LIST[$currency_code])) {
            return self::CURRENCY_SYMBOL_LIST[$currency_code];
        } else {
            return $currency_code;
        }
    }

